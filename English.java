    static String cipherText(String str, String key)    //Encryption Function 
    { 
        String cipher_text=""; 
        str = str.toUpperCase();                        //Convert letters to capital letters
      for (int i = 0; i < str.length(); i++)            
      { 
        int x = (str.charAt(i) + key.charAt(i)) %26;    //Algorithm
        x += 'A';                                       //equation to get value of each letter regarding to A position in ASCII
  
        cipher_text+=(char)(x);                         //Get Cipher Text
    } 
    return cipher_text; 
} 
  
    static String originalText(String cipher_text, String key)  //Decryption Function 
    { 
        String orig_text=""; 
 
        for (int i = 0 ; i < cipher_text.length(); i++) 
        { 
           int x = (cipher_text.charAt(i) -  key.charAt(i) + 26) %26;     //Decryption Formula
           x += 'A';                                    //equation to get value of each letter regarding to A position in ASCII
           orig_text+=(char)(x);                        //Get Plain Text
    } 
    return orig_text; 
    }
 
