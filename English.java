    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
             String keyword = Key.getText();        //Get Key
             String str = Msg_Text.getText();       //Get Message
              int len=str.length();
               String strMsg="";
              for (int i=0; i<len; i++)             //Delete Space
              {
                  if (str.charAt(i)== ' ')
                      continue;
                  strMsg+=str.charAt(i);
              }

              String key = generateKey(strMsg, keyword);      //Call GenerateKey Function 
              String cipher_text = cipherText(strMsg, key);   //Call CipherText Function
              Encry_Message.setText(cipher_text);             //Output Cipher text message
    }                                        

